// linguagem web Fon

/*
+ - Adição
- - Subtração
* - Multiplicação
/ - Divisão
% - Resto ou Modulo
++ - Incremento
-- - Decremento
*/

var variavelAleatoria //undefined

//Adição
variavelAleatoria = 2+1 //3

//Subtração
variavelAleatoria = 2-1 //1

//Multiplicação
variavelAleatoria = 2*2 //4

//Divisão
variavelAleatoria = 2/2 //1

//Resto ou Modulo
variavelAleatoria = 5%2 //1

//Incremento
variavelAleatoria = 2++ //3
variavelAleatoria++ //4

//Decremento
variavelAleatoria = 2-- //1
variavelAleatoria-- //0

